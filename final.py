'''Описание задачи
Ваша задача — улучшить структуру хранения данных заметки, используя списки.
Это позволит организовать информацию более эффективно и удобно.
Вместо создания отдельных переменных для каждого элемента заметки вы будете использовать один список,
который будет содержать все необходимые данные, включая заголовки заметок.

Преимущества использования списков:
Организация данных: Все связанные данные хранятся в одном месте, что делает код более структурированным.
Удобство доступа: Доступ к данным осуществляется по индексу, что упрощает чтение и изменение значений.
Легкость расширения: Легко добавлять новые поля, просто добавляя новые элементы в список.
Упрощение передачи данных: Списки удобно передавать в функции или возвращать из них.
Читаемость: Код становится более понятным, так как все данные находятся в одном месте.
Шаги выполнения:
Создайте список для хранения информации о заметке.
Соберите данные от пользователя: имя, содержание, статус, даты и заголовки.
Добавьте заголовки в отдельный вложенный список внутри основного списка.
Выведите собранные данные на экран.
В результате мы получим единый формат хранения заметок, что значительно упростит работу с ними,
особенно когда их станет много. Например, мы сможем передавать заметки в функции и хранить
несколько из них в одном списке. Если бы мы использовали обычные переменные для хранения информации о каждой заметке,
это было бы затруднительно.
Пример структуры данных с использованием списка:
note = [
"Имя пользователя",
"Содержание заметки",
"Статус",
"Дата создания",
"Дата изменения",
["Заголовок 1", "Заголовок 2"] # вложенный список для заголовков]'''

# здесь пользователь вводит свое имя
username = input('Введите имя пользователя: ')

# здесь пользователь добавляет три заголовка заметки с добавлением всех заголовков в общий список
first_title = input('Введите первый заголовок заметки: ')
second_title = input('Введите второй заголовок заметки: ')
third_title = input('Введите третий заголовок заметки: ')
title = [first_title, second_title, third_title]

# здесь пользователь вводит описание заметки
content = input('Введите описание заметки: ')

# здесь пользователь вводит статус заметки
status = input('Укажите на каком этапе выполнения находится ваша заметка: \n')

# здесь пользователь вводит дату создания заметки в формате "день-месяц-год", например "10-11-2024"
created_date = input('Введите дату создания заметки в формате день-месяц-год, например, 01-01-2025: ')

# здесь пользователь вводит дату истечения (дедлайна) заметки в формате "день-месяц-год", например "10-11-2024"
issue_date = input('Введите дату, до которой заметка будет выполнена, в формате день-месяц-год, например, 01-02-2025: ')

# здесь добавлена переменная note, которая включает в себя все вышеуказанные переменные в один список
note = [username, content, status, created_date, issue_date, title]
print(note)

